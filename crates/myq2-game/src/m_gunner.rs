// ============================================================

fn gunner_fire_think(self_ent: &mut Edict, _ctx: &mut GameContext) {
    let flash_number = MZ2_GUNNER_MACHINEGUN_1 + (self_ent.s.frame - FRAME_ATTAK216);

    let mut forward = [0.0_f32; 3];
    let mut right = [0.0_f32; 3];
    angle_vectors(&self_ent.s.angles, Some(&mut forward), Some(&mut right), None);

    let offset = monster_flash_offset(flash_number);
    let mut start = [0.0_f32; 3];
    g_project_source(&self_ent.s.origin, &offset, &forward, &right, &mut start);

    // project enemy back a bit and target there
    let enemy_idx = self_ent.enemy;
    let mut target = [0.0_f32; 3];
    if enemy_idx > 0 && (enemy_idx as usize) < _ctx.edicts.len() {
        let enemy = &_ctx.edicts[enemy_idx as usize];
        target = vector_ma(&enemy.s.origin, -0.2, &enemy.velocity);
        target[2] += enemy.viewheight as f32;
    }

    let mut aim = [0.0_f32; 3];
    aim[0] = target[0] - start[0];
    aim[1] = target[1] - start[1];
    aim[2] = target[2] - start[2];
    vector_normalize(&mut aim);
    monster_fire_bullet(self_ent, start, aim, 3, 4, DEFAULT_BULLET_HSPREAD, DEFAULT_BULLET_VSPREAD, flash_number);
}

// ============================================================
// GunnerGrenade — grenade attack
// ============================================================

fn gunner_grenade(self_ent: &mut Edict, _ctx: &mut GameContext) {
    let flash_number = if self_ent.s.frame == FRAME_ATTAK105 {
        MZ2_GUNNER_GRENADE_1
    } else if self_ent.s.frame == FRAME_ATTAK108 {
        MZ2_GUNNER_GRENADE_2
    } else if self_ent.s.frame == FRAME_ATTAK111 {
        MZ2_GUNNER_GRENADE_3
    } else {
        // self->s.frame == FRAME_attak114
        MZ2_GUNNER_GRENADE_4
    };

    let mut forward = [0.0_f32; 3];
    let mut right = [0.0_f32; 3];
    angle_vectors(&self_ent.s.angles, Some(&mut forward), Some(&mut right), None);

    let offset = monster_flash_offset(flash_number);
    let mut start = [0.0_f32; 3];
    g_project_source(&self_ent.s.origin, &offset, &forward, &right, &mut start);

    // Fires straight forward (spread pattern not implemented)
    let aim = forward;

    monster_fire_grenade(self_ent, start, aim, 50, 600, flash_number);
}

