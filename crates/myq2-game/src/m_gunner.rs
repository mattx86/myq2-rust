// ============================================================
// Attack decision
// ============================================================

pub fn gunner_attack(self_ent: &mut Edict, _ctx: &mut GameContext) {
    let enemy_idx = self_ent.enemy;
    let r = if enemy_idx > 0 && (enemy_idx as usize) < _ctx.edicts.len() {
        range(self_ent, &_ctx.edicts[enemy_idx as usize])
    } else {
        RANGE_FAR
    };

    if r == RANGE_MELEE {
        // In melee range, use chain gun
        self_ent.monsterinfo.currentmove = Some(MOVE_ATTACK_CHAIN);
    } else if random() <= 0.5 {
        self_ent.monsterinfo.currentmove = Some(MOVE_ATTACK_GRENADE);
    } else {
        self_ent.monsterinfo.currentmove = Some(MOVE_ATTACK_CHAIN);
    }
}

pub fn gunner_fire_chain(self_ent: &mut Edict, _ctx: &mut GameContext) {
    self_ent.monsterinfo.currentmove = Some(MOVE_FIRE_CHAIN);
}

pub fn gunner_refire_chain(self_ent: &mut Edict, _ctx: &mut GameContext) {
    let enemy_idx = self_ent.enemy;
    if enemy_idx > 0 && (enemy_idx as usize) < _ctx.edicts.len() {
        let enemy = &_ctx.edicts[enemy_idx as usize];
        if enemy.health > 0 && visible(self_ent, enemy) && random() <= 0.5 {
            self_ent.monsterinfo.currentmove = Some(MOVE_FIRE_CHAIN);
            return;
        }
    }
    self_ent.monsterinfo.currentmove = Some(MOVE_ENDFIRE_CHAIN);
}

